'use strict'

const fs   = require('fs')
const path = require('path')

const { c, log, hasFlag, stripAnsi, header, validateManifestPath } = require('./shared')
const { diffManifests }                       = require('./diff/diff-engine')
const { renderDiff, renderDiffJson }          = require('./diff/diff-render')

/**
 * cmd-diff.js — diff command entry point.
 *
 * Usage:
 *   capman-studio diff <old-manifest> <new-manifest>
 *   capman-studio diff <old-manifest> <new-manifest> --verbose
 *   capman-studio diff <old-manifest> <new-manifest> --json
 *   capman-studio diff <old-manifest> <new-manifest> --unchanged
 *
 * Exit codes:
 *   0 — manifests are identical
 *   1 — manifests differ (or an error occurred)
 *
 * ADR-005: reads manifest JSON files directly via fs.readFileSync + JSON.parse.
 * Does not import capman internals. manifest.json is a stable data format.
 * Reading JSON files is pure Node.js with no capman dependency.
 */
function cmdDiff() {
  header()

  const jsonMode        = hasFlag('--json')
  const verbose         = hasFlag('--verbose')
  const includeUnchanged = hasFlag('--unchanged')

  // ── Positional args ────────────────────────────────────────────────────────
  // args = process.argv.slice(2), index 0 is 'diff'
  // positional paths start at index 1 and 2
  const rawArgs = process.argv.slice(2)
  const positional = rawArgs.filter(a => !a.startsWith('-'))

  const oldPath = positional[1]  // index 0 is 'diff'
  const newPath = positional[2]

  if (!oldPath || !newPath) {
    log.error('Two manifest paths are required.')
    console.log()
    console.log(`  ${c.gray}Usage: capman-studio diff <old-manifest> <new-manifest>${c.reset}`)
    console.log(`  ${c.gray}Example: capman-studio diff manifest-v1.json manifest-v2.json${c.reset}`)
    console.log()
    process.exit(1)
  }

  // ── Load both manifests ────────────────────────────────────────────────────
  const oldManifest = loadManifest(oldPath, 'old')
  const newManifest = loadManifest(newPath, 'new')

  // ── Run diff ───────────────────────────────────────────────────────────────
  const result = diffManifests(oldManifest, newManifest, { includeUnchanged })

  // ── Render ─────────────────────────────────────────────────────────────────
  if (jsonMode) {
    renderDiffJson(result)
  } else {
    renderDiff(result, { verbose, includeUnchanged })
  }

  // ── Exit code ──────────────────────────────────────────────────────────────
  // Exit 1 when manifests differ — allows use as a CI change-detection gate:
  //   capman-studio diff manifest-before.json manifest-after.json || echo "manifest changed"
  if (result.hasChanges) process.exit(1)
}

// ─── Manifest Loader ──────────────────────────────────────────────────────────

/**
 * Load and parse a manifest JSON file.
 * Validates path is within CWD (symlink-aware) before reading.
 * Never surfaces raw JSON parse errors — prevents partial content leaking.
 *
 * @param {string} rawPath  - path as supplied by user
 * @param {string} label    - 'old' or 'new' — for error messages
 * @returns {object} parsed manifest
 */
function loadManifest(rawPath, label) {
  // ── Symlink-aware CWD restriction ─────────────────────────────────────────
  // Uses the shared validateManifestPath — symlink-aware, consistent with
  // all other file-reading paths in capman-studio (eval-suite, eval-inspect).
  // validateManifestPath exits with a clear error if the path escapes CWD.
  validateManifestPath(rawPath)

  const resolved = path.resolve(rawPath)

  // ── Read file ──────────────────────────────────────────────────────────────
  let raw
  try {
    raw = fs.readFileSync(resolved, 'utf8')
  } catch (e) {
    log.error(`Could not read ${label} manifest: ${stripAnsi(e.code ?? 'unknown error')}`)
    log.hint(`Path: ${resolved}`)
    process.exit(1)
  }

  // ── Parse JSON ─────────────────────────────────────────────────────────────
  let manifest
  try {
    manifest = JSON.parse(raw)
  } catch {
    // Do not surface e.message — JSON parse errors include partial file
    // content which could leak secrets from a misdirected path.
    log.error(`${label} manifest is not valid JSON: ${path.basename(rawPath)}`)
    log.hint(`Validate it with: capman validate --manifest ${rawPath}`)
    process.exit(1)
  }

  // ── Basic shape check ──────────────────────────────────────────────────────
  if (!manifest || typeof manifest !== 'object' || !Array.isArray(manifest.capabilities)) {
    log.error(`${label} manifest is missing a "capabilities" array: ${path.basename(rawPath)}`)
    log.hint(`A valid manifest.json is generated by: capman generate`)
    process.exit(1)
  }

  return manifest
}

// ─── Exports ──────────────────────────────────────────────────────────────────

module.exports = { cmdDiff }